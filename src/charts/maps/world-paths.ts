function buildCountry(id: string): CountryPath {
  const def = countryCoords[id];
  return {
    id,
    name: def.name,
    path: poly(def.coords),
    centroid: centroid(def.coords),
  };
}

export const WORLD_COUNTRIES: CountryPath[] = Object.keys(countryCoords).map(buildCountry);

export const COUNTRY_MAP: Record<string, CountryPath> = Object.fromEntries(
  WORLD_COUNTRIES.map((c) => [c.id, c]),
);
