// Simplified world map SVG paths for ~30 major countries
// ViewBox: 0 0 1000 500 using a simple equirectangular/Mercator-like projection
// Longitude -180..180 -> x 0..1000, Latitude ~80..-60 -> y 0..500

export interface CountryPath {
  id: string;       // ISO 3166-1 alpha-2
  name: string;
  path: string;     // SVG path d attribute
  centroid: [number, number]; // center point for labels/bubbles
}

// Helper: convert [lon, lat] to [x, y] in our viewBox
function p(lon: number, lat: number): [number, number] {
  const x = ((lon + 180) / 360) * 1000;
  const y = ((80 - lat) / 140) * 500;
  return [Math.round(x * 10) / 10, Math.round(y * 10) / 10];
}

function poly(coords: [number, number][]): string {
  const pts = coords.map(([lon, lat]) => p(lon, lat));
  return 'M' + pts.map(([x, y]) => `${x},${y}`).join(' L') + ' Z';
}

function centroid(coords: [number, number][]): [number, number] {
  const pts = coords.map(([lon, lat]) => p(lon, lat));
  const cx = pts.reduce((s, pt) => s + pt[0], 0) / pts.length;
  const cy = pts.reduce((s, pt) => s + pt[1], 0) / pts.length;
  return [Math.round(cx), Math.round(cy)];
}

